def string_to_array(s):
    print(s.split(" "))
    return s.split(" ")


string_to_array("Robin Singh")
string_to_array("CodeWars")
string_to_array("I love arrays they are my favorite")
string_to_array("1 2 3")

# ["Robin", "Sinh"]
# ["CodeWars"]
# ["I", "love", "arrays", "they", "are", "my", "favorite"]
# ["1", "2", "3"]